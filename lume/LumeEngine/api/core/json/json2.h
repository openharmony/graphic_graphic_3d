namespace {
[[nodiscard]] int codepoint(BASE_NS::string_view str)
{
    int code = 0;
    for (size_t u = 0U; u < 4U; ++u) {
        const char chr = str[u];
        code <<= 4;  // Shift by the size of a single hex character
        code += BASE_NS::HexToDec(chr);
    }
    return code;
}
}  // namespace

