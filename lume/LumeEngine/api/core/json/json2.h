template <typename T, typename>
[[nodiscard]] value_t<T> parse(BASE_NS::string_view data)
{
    using jsonValue = value_t<T>;
    typename jsonValue::array_t stack;
    // push an uninitialized value which will get the final value during parsing
    stack.emplace_back();

    bool acceptValue = true;
    while (!data.empty()) {
        data = SkipWhiteSpaces(data);
        if (data.empty()) {
            return {};  // unexpected end
        }
        if (data.front() == '{') {
            // start of an object
            if (!acceptValue) {
                return {};
            }
            data.remove_prefix(1);
            data = SkipWhiteSpaces(data);
            if (data.starts_with('}')) {
                data.remove_prefix(1);
                data = SkipWhiteSpaces(data);
                // handle empty object.
                add(stack.back(), jsonValue::object());
                acceptValue = false;
            } else if (data.starts_with('"')) {
                // try to read the key
                jsonValue key;
                data.remove_prefix(1);
                data = parse_string(data, key);
                if (!key.is_string()) {
                    return {};  // failed to parse key
                }
                data = SkipWhiteSpaces(data);

                if (!data.starts_with(':')) {
                    // missing : after key
                    return {};
                }
                data.remove_prefix(1);
                data = SkipWhiteSpaces(data);
                // push the object with key and missing value on the stack and hope to find a value next
                stack.emplace_back(jsonValue::object({
                    {BASE_NS::move(key.as_escaped_string()), jsonValue{}},
                }));

                acceptValue = true;
            } else {
                // missing start of key or end of object
                return {};
            }
        } else if (data.front() == '}') {
            // end of an object
            if (stack.back().type() != type::object) {
                // unexpected }
                return {};
            }
            // check are we missing a value ('{"":}', '{"":"",}' )
            if (acceptValue) {
                return {};
            }
            data.remove_prefix(1);
            data = SkipWhiteSpaces(data);
            // move this object to the next in the stack
            auto value = BASE_NS::move(stack.back());
            stack.pop_back();
            if (stack.empty()) {
                // invalid JSON
                return {};
            }
            add(stack.back(), BASE_NS::move(value));
            acceptValue = false;
        } else if (data.front() == '[') {
            // start of an array
            if (!acceptValue) {
                // unexpected [
                return {};
            }
            data.remove_prefix(1);
            data = SkipWhiteSpaces(data);
            if (data.empty()) {
                return {};  // unexpected end
            }
            if (data.front() == ']') {
                data.remove_prefix(1);
                data = SkipWhiteSpaces(data);
                // handle empty array.
                add(stack.back(), jsonValue::array());
                acceptValue = false;
            } else {
                // push the empty array on the stack and hope to find values
                stack.push_back(jsonValue::array());
                acceptValue = true;
            }
        } else if (data.front() == ']') {
            // end of an array
            if (stack.back().type() != type::array) {
                // unexpected ]
                return {};
            }
            // check are we missing a value ('[1,]' '[1]]')
            if (acceptValue) {
                // unexpected ]
                return {};
            }
            data.remove_prefix(1);
            data = SkipWhiteSpaces(data);

            auto value = BASE_NS::move(stack.back());
            stack.pop_back();
            if (stack.empty()) {
                // invalid JSON
                return {};
            }
            add(stack.back(), BASE_NS::move(value));
            acceptValue = false;
        } else if (data.front() == ',') {
            // comma is allowed when the previous value was complete and we have an incomplete object or array on the
            // stack.
            if (!acceptValue && stack.back().type() == type::object) {
                data.remove_prefix(1);
                data = SkipWhiteSpaces(data);
                if (data.empty()) {
                    return {};  // unexpected end
                }
                if (data.front() != '"') {
                    // missing key for next object
                    return {};
                }

                // try to read the key
                jsonValue key;
                data.remove_prefix(1);
                data = parse_string(data, key);
                if (!key.is_string()) {
                    return {};  // failed to parse key
                }
                data = SkipWhiteSpaces(data);

                if (!data.starts_with(':')) {
                    // missing value for next object
                    return {};
                }
                data.remove_prefix(1);
                data = SkipWhiteSpaces(data);
                stack.back().as_object().emplace_back(BASE_NS::move(key.as_escaped_string()), jsonValue{});
                acceptValue = true;
            } else if (!acceptValue && stack.back().type() == type::array) {
                data.remove_prefix(1);
                data = SkipWhiteSpaces(data);
                acceptValue = true;
            } else {
                // comma allowed only between objects and values inside an array
                return {};
            }
        } else if (data.front() == '"') {
            if (!acceptValue) {
                return {};  // unexpeceted "
            }
            jsonValue value;
            data.remove_prefix(1);
            data = parse_string(data, value);
            if (value.type() != type::string) {
                return {};  // invalid string literal
            }

            add(stack.back(), BASE_NS::move(value));
            acceptValue = false;
        } else if (IsSign(data.front()) || IsDigit(data.front())) {
            jsonValue value;
            data = parse_number(data, value);
            if (acceptValue && value.type() != type::uninitialized) {
                add(stack.back(), BASE_NS::move(value));
                acceptValue = false;
            } else {
                // failed parsing number
                return {};
            }
        } else if (data.starts_with(TRUE_STR)) {
            if (!acceptValue) {
                return {};  // value was not expected
            }
            add(stack.back(), jsonValue::boolean(true));
            acceptValue = false;
            data.remove_prefix(TRUE_STR.size());
        } else if (data.starts_with(FALSE_STR)) {
            if (!acceptValue) {
                return {};  // value was not expected
            }
            add(stack.back(), jsonValue::boolean(false));
            acceptValue = false;
            data.remove_prefix(FALSE_STR.size());
        } else if (data.starts_with(NULL_STR)) {
            if (!acceptValue) {
                return {};  // value was not expected
            }
            add(stack.back(), jsonValue::null());
            acceptValue = false;
            data.remove_prefix(NULL_STR.size());
        } else {
            // unexpected character
            return {};
        }
    }
    // check if we are missing a value ('{"":' '[')
    if (acceptValue) {
        return {};
    }

    auto value = BASE_NS::move(stack.front());
    return value;
}

