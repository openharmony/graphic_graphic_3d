[[nodiscard]] inline bool IsWhite(char data)
{
    return ((data == ' ') || (data == '\n') || (data == '\r') || (data == '\t'));
}

[[nodiscard]] inline bool IsSign(char data)
{
    return ((data == '+') || (data == '-'));
}

[[nodiscard]] inline bool IsDigit(char data)
{
    return ((data >= '0') && (data <= '9'));
}

[[nodiscard]] inline bool IsHex(char data)
{
    return ((data >= '0') && (data <= '9')) || ((data >= 'a') && (data <= 'f')) || ((data >= 'A') && (data <= 'F'));
}

[[nodiscard]] inline BASE_NS::string_view SkipWhiteSpaces(BASE_NS::string_view data)
{
    while (!data.empty() && IsWhite(data.front())) {
        data.remove_prefix(1);
    }
    return data;
}

