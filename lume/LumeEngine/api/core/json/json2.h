BASE_NS::string to_string(const value_t<T>& value)
{
    BASE_NS::string out;
    to_string(out, value);
    return out;
}

template <typename T>
void to_string(BASE_NS::string& out, const value_t<T>& value)
{
    switch (value.type()) {
        case type::uninitialized:
            break;
        case type::object:
            append<T>(out, value.as_object());
            break;
        case type::array:
            append<T>(out, value.as_array());
            break;
        case type::string:
            append<T>(out, value.as_escaped_string());
            break;
        case type::floating_point:
            append<T>(out, value.as_strict_floating_point());
            break;
        case type::signed_int:
            out += BASE_NS::to_string(value.as_strict_signed_int());
            break;
        case type::unsigned_int:
            out += BASE_NS::to_string(value.as_strict_unsigned_int());
            break;
        case type::boolean:
            if (value.as_boolean()) {
                out += TRUE_STR;
            } else {
                out += FALSE_STR;
            }
            break;
        case type::null:
            out += NULL_STR;
            break;
        default:
            CORE_ASSERT_MSG(false, "Unexpected JSON type");
            break;
    }
}

