void Detail::unescape(BASE_NS::string& out, BASE_NS::string_view str)
{
    out.reserve(out.size() + str.size());
    for (size_t i = 0, end = str.size(); i < end;) {
        auto chr = str[i];
        ++i;
        if (chr != '\\') {
            out += chr;
        } else {
            chr = str[i];
            ++i;
            if (chr == '"') {
                out += '"';  // Quotation mark
            } else if (chr == '\\') {
                out += '\\';  // Reverse solidus
            } else if (chr == '/') {
                out += '/';  // Solidus.. we do unescape this..
            } else if (chr == 'b') {
                out += '\b';  // Backspace
            } else if (chr == 'f') {
                out += '\f';  // Formfeed
            } else if (chr == 'n') {
                out += '\n';  // Linefeed
            } else if (chr == 'r') {
                out += '\r';  // Carriage return
            } else if (chr == 't') {
                out += '\t';            // Horizontal tab
            } else if (chr == 'u') {    // Unicode character
                if ((i + 4U) <= end) {  // Expecting 4 hexadecimal values
                    // Read the Unicode code point.
                    int code = codepoint(str.substr(i, 4U));
                    i += 4U;
                    // Codepoints U+010000 to U+10FFFF are encoded as UTF-16 surrogate pairs. High surrogate between
                    // 0xD800-0xDBFF and low between 0xDC00-0xDFFF.
                    if (code >= 0xd800 && code <= 0xdbff) {
                        // See if there's an other \uXXXX value in the correct range.
                        if ((i + 6U) <= end) {
                            auto next = str.substr(i, 6U);
                            if (next[0] == '\\' && next[1] == 'u') {
                                next.remove_prefix(2);
                                int low = codepoint(next);
                                i += 6U;
                                if (low >= 0xdc00 && low <= 0xdfff) {
                                    // Surrogate pair encoding: 0x10000 + (Hi - 0xD800) * 0x400 + (Lo - 0xDC00)
                                    code = (static_cast<int>(static_cast<unsigned int>(code) << 10U) + low -
                                            (static_cast<int>(0xd800U << 10U) + 0xdc00 - 0x10000));
                                }
                            }
                        }
                    }
                    // Convert code point to UTF-8.
                    if (code < 0x80) {
                        // 1-byte characters: 0xxxxxxx (ASCII)
                        out += static_cast<char>(code);
                    } else if (code < 0x7ff) {
                        // 2-byte characters: 110xxxxx 10xxxxxx
                        out += static_cast<char>(0xc0U | (static_cast<unsigned int>(code) >> 6U));
                        out += static_cast<char>(0x80U | (static_cast<unsigned int>(code) & 0x3fU));
                    } else if (code <= 0xffff) {
                        // 3-byte characters: 1110xxxx 10xxxxxx 10xxxxxx
                        out += static_cast<char>(0xe0U | (static_cast<unsigned int>(code) >> 12u));
                        out += static_cast<char>(0x80U | ((static_cast<unsigned int>(code) >> 6U) & 0x3FU));
                        out += static_cast<char>(0x80U | (static_cast<unsigned int>(code) & 0x3Fu));
                    } else {
                        // 4-byte characters: 11110xxx 10xxxxxx 10xxxxxx 10xxxxxx
                        out += (static_cast<char>(0xf0U | (static_cast<unsigned int>(code) >> 18U)));
                        out += (static_cast<char>(0x80U | ((static_cast<unsigned int>(code) >> 12U) & 0x3fU)));
                        out += (static_cast<char>(0x80U | ((static_cast<unsigned int>(code) >> 6U) & 0x3fU)));
                        out += (static_cast<char>(0x80U | (static_cast<unsigned int>(code) & 0x3fU)));
                    }
                }
            }
        }
    }
}

