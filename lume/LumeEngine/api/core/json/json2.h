void Detail::escape(BASE_NS::string& out, BASE_NS::string_view str)
{
    out.reserve(out.size() + str.size());
    for (size_t i = 0, end = str.size(); i < end;) {
        auto chr = static_cast<uint8_t>(str[i]);
        ++i;
        if (chr == '"') {
            out += "\\\"";  // Quotation mark
        } else if (chr == '\\') {
            out += "\\\\";  // Reverse solidus
        } else if (chr == '\b') {
            out += "\\b";  // Backspace
        } else if (chr == '\f') {
            out += "\\f";  // Formfeed
        } else if (chr == '\n') {
            out += "\\n";  // Linefeed
        } else if (chr == '\r') {
            out += "\\r";  // Carriage return
        } else if (chr == '\t') {
            out += "\\t";  // Horizontal tab
        } else if (chr < 0x80) {
            out += static_cast<BASE_NS::string::value_type>(chr);  // 1-byte characters: 0xxxxxxx (ASCII)
        } else {
            // Unicode
            unsigned code = 0U;
            unsigned left = 0U;
            // Decode first byte and figure out how many additional bytes are needed for the codepoint.
            if (chr < 0xE0) {
                // 2-byte characters: 110xxxxx 10xxxxxx
                if (i < end) {
                    code = ((chr & ~0xC0U));
                    left = 1U;
                }
            } else if (chr < 0xF0) {
                // 3-byte characters: 1110xxxx 10xxxxxx 10xxxxxx
                if ((i + 1U) < end) {
                    code = (chr & ~0xE0U);
                    left = 2U;
                }
            } else if (chr < 0xF8) {
                // 4-byte characters: 11110xxx 10xxxxxx 10xxxxxx 10xxxxxx
                if ((i + 2U) < end) {
                    code = (chr & ~0xF0U);
                    left = 3U;
                }
            }

            // Combine the remaining bytes.
            while (left--) {
                code = (code << 6U) + (str[i++] & 0x3FU);
            }

            // Codepoints U+010000 to U+10FFFF are encoded as UTF-16 surrogate pairs.
            // Surrogate pair encoding: 0x10000 + (Hi - 0xD800) * 0x400 + (Lo - 0xDC00)
            if (code >= 0x010000 && code <= 0x10FFFF) {
                // First append the Hi value.
                code -= 0x10000U;
                const auto hi = (code >> 10U) + 0xD800U;
                out += UNISTART_STR;
                out += BASE_NS::to_hex(hi);

                // Calculate the Lo value.
                code = (code & 0x3FFU) + 0xDC00U;
            }

            // Append the codepoint zero padded to four hex values.
            out += UNISTART_STR;
            const auto codepoint = BASE_NS::to_hex(code);
            if (codepoint.size() < 4U) {
                out.append(4U - codepoint.size(), '0');
            }
            out += codepoint;
        }
    }
}

