// Definition of the template operator== for JSON values.
template <typename Tag1, typename Tag2>
bool operator==(const value_t<Tag1>& lhs, const value_t<Tag2>& rhs)
{
    if (lhs.type() != rhs.type()) {
        return false;
    }
    switch (lhs.type()) {
        case type::uninitialized:
            return true;
        case type::object: {
            const auto& lhs_object = lhs.as_object();
            const auto& rhs_object = rhs.as_object();
            if (lhs_object.size() != rhs_object.size()) {
                return false;
            }
            for (const auto& lkv : lhs_object) {
                const auto* matchedValue = rhs.find(lkv.escapedKey);
                if (!matchedValue || (lkv.value != *matchedValue)) {
                    return false;
                }
            }
            for (const auto& rkv : rhs_object) {
                const auto* matchedValue = lhs.find(rkv.escapedKey);
                if (!matchedValue || (rkv.value != *matchedValue)) {
                    return false;
                }
            }
            return true;
        }
        case type::array: {
            const auto& lhs_array = lhs.as_array();
            const auto& rhs_array = rhs.as_array();
            if (lhs_array.size() != rhs_array.size()) {
                return false;
            }
            for (size_t i = 0; i < lhs_array.size(); ++i) {
                if (!(lhs_array[i] == rhs_array[i])) {
                    return false;
                }
            }
            return true;
        }
        case type::string:
            return lhs.as_escaped_string() == rhs.as_escaped_string();
        case type::floating_point:
            return lhs.as_strict_floating_point() == rhs.as_strict_floating_point();
        case type::signed_int:
            return lhs.as_strict_signed_int() == rhs.as_strict_signed_int();
        case type::unsigned_int:
            return lhs.as_strict_unsigned_int() == rhs.as_strict_unsigned_int();
        case type::boolean:
            return lhs.as_boolean() == rhs.as_boolean();
        case type::null:
            return true;
        default:
            CORE_ASSERT_MSG(false, "Unexpected JSON type");
            return false;
    }
}

template <typename Tag1, typename Tag2>
bool operator!=(const value_t<Tag1>& lhs, const value_t<Tag2>& rhs)
{
    return !(lhs == rhs);
}

