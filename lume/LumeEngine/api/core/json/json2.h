/// Parses a string literal into `res`, returning the string view of the remainder of the parseable string.
///
/// Expects data to start after the opening "
template <typename Tag>
[[nodiscard]] BASE_NS::string_view parse_string(BASE_NS::string_view data, value_t<Tag>& res)
{
    const auto start = data;
    while (!data.empty()) {
        if (data.front() == '\\' && data.size() >= 2) {
            // escape.. (parse just enough to not stop too early)
            if (data[1] == '\\' || data[1] == '"' || data[1] == '/' || data[1] == 'b' || data[1] == 'f' ||
                data[1] == 'n' || data[1] == 'r' || data[1] == 't') {
                data.remove_prefix(2);
                continue;
            } else if (data[1] == 'u') {
                data.remove_prefix(2);
                if (data.size() < 4) {
                    return data;
                }
                for (char i = 0; i < 4; ++i) {
                    if (data.empty() || !IsHex(data.front())) {
                        // string ended too early or it's an invalid unicode.
                        return data;
                    }
                    data.remove_prefix(1);
                }
            } else {
                // invalid escape
                return data;
            }
        } else if (data.front() == '"') {
            using EscapedString = typename value_t<Tag>::escaped_string_t;
            using String = conditionally_writable_string_t<Tag>;

            res = value_t<Tag>::escaped_string(
                EscapedString(String{start.data(), static_cast<size_t>(data.data() - start.data())}));
            data.remove_prefix(1);
            return data;
        } else if (static_cast<unsigned char>(data.front()) < 0x20) {
            // unexpected unescaped control
            return data;
        } else {
            data.remove_prefix(1);
        }
    }

    // unexpected end
    return data;
}

template <typename Tag>
[[nodiscard]] BASE_NS::string_view parse_number(BASE_NS::string_view data, value_t<Tag>& res)
{
    CORE_ASSERT_MSG(!data.empty() && (IsDigit(data.front()) || IsSign(data.front())),
        "Precondition: first digit is either a digit or a sign");

    bool negative = false;
    const auto beg = data;
    if (data.front() == '-') {
        negative = true;
        data.remove_prefix(1);
        if (data.empty() || !IsDigit(data.front())) {
            // no digits after '-'
            return data;
        }
    }
    bool fraction = false;
    bool exponent = false;

    if (data.front() == '0') {
        data.remove_prefix(1);
        // after leading zero only '.', 'e' and 'E' allowed
        if (!data.empty()) {
            if (data.front() == '.') {
                data.remove_prefix(1);
                fraction = true;
            } else if (data.front() == 'e' || data.front() == 'E') {
                data.remove_prefix(1);
                exponent = true;
            }
        }
    } else {
        while (!data.empty() && IsDigit(data.front())) {
            data.remove_prefix(1);
        }
        if (data.starts_with('.')) {
            data.remove_prefix(1);
            fraction = true;
        } else if (data.starts_with('e') || data.starts_with('E')) {
            data.remove_prefix(1);
            exponent = true;
        }
    }

    if (fraction) {
        // fraction must start with a digit
        if (!data.empty() && IsDigit(data.front())) {
            data.remove_prefix(1);
        } else {
            // fraction missing first digit
            return data;
        }
        // fraction may contain digits up to begining of exponent ('e' or 'E')
        while (!data.empty() && IsDigit(data.front())) {
            data.remove_prefix(1);
        }
        if (data.starts_with('e') || data.starts_with('E')) {
            data.remove_prefix(1);
            exponent = true;
        }
    }
    if (exponent) {
        // exponent must start with '-' or '+' followed by a digit, or digit
        if (data.starts_with('-') || data.starts_with('+')) {
            data.remove_prefix(1);
        }
        if (!data.empty() && IsDigit(data.front())) {
            data.remove_prefix(1);
        } else {
            // exponent missing first digit
            return data;
        }
        while (!data.empty() && IsDigit(data.front())) {
            data.remove_prefix(1);
        }
    }

    if (fraction || exponent) {
        auto distance = data.data() - beg.data();
        if (distance > 64) {
            return data;  // too large number
        }
        BASE_NS::fixed_string<64> stackNullTerminatedString =
            BASE_NS::string_view{beg.data(), static_cast<size_t>(distance)};

        char* pointerAfterParse = nullptr;
        // Assuming this will succeed, as we have already parsed the nummber above.
        double result = std::strtod(stackNullTerminatedString.data(), &pointerAfterParse);
        res = value_t<Tag>::number(result);
        return data;
    } else if (negative) {
        long long result;
        if (std::from_chars(beg.data(), data.data(), result).ec != std::errc{}) {
            return data;
        }
        res = value_t<Tag>::number(result);
        return data;
    }

    unsigned long long result;
    if (std::from_chars(beg.data(), data.data(), result).ec != std::errc{}) {
        return data;
    }
    res = value_t<Tag>::number(result);
    return data;
}

template <typename T>
void add(value_t<T>& v, value_t<T>&& value)
{
    switch (v.type()) {
        case type::uninitialized:
            v = BASE_NS::move(value);
            break;
        case type::object:
            v.as_object().back().value = BASE_NS::move(value);
            break;
        case type::array:
            v.as_array().push_back(BASE_NS::move(value));
            break;
        case type::string:
        case type::floating_point:
        case type::signed_int:
        case type::unsigned_int:
        case type::boolean:
        case type::null:
        default:
            break;
    }
}

